% ------------------------------ BEGIN CODE -------------------------------

    pos = @(x) exp(x);
   

    k_1 =pos(-0.72653725);
    k_3 =pos(0.65442913);
    k_5 =pos(0.41001916);
    k_7 =pos(-0.37504567);
    k_9 =pos(-0.07942825);
    k_11 =pos(0.07779509);
    k_13 =pos(-1.10721573);
    
