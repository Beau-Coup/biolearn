    k_4 = pos(0.15646746);
    k_6 = pos(1.37422567);
    k_8 = pos(0.5345936);
    k_10 = pos(0.52311874);
    k_12 = pos(-0.0692791);
    k_14 =pos(-0.38075173);

    dx(1,1) = k_1*x(3)-k_2*x(1);
    dx(2,1) = k_3*x(5)-k_4*x(2);
    dx(3,1) = k_5*x(7)-k_6*x(3)*x(2);
    dx(4,1) = k_7-k_8*x(4)*x(3);
    dx(5,1) = k_9*x(1)-k_10*x(4)*x(5);
    dx(6,1) = k_11*x(1)-k_12*x(6);
